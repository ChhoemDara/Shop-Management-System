    private void txtReceivedKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtReceivedKeyTyped
        try{
            DecimalFormat df=new DecimalFormat("0.##");
            MyInput.inputFloating(evt, txtReceived, 5);
            if(evt.getKeyChar()==10){
            double received=Double.valueOf(txtReceived.getText());
            double total=Double.valueOf(txtGrandTotal.getText());
            if(received>=total){
                double returned=received - total;
                txtReturned.setText(""+df.format(returned));
                txtReturnedR.setText(""+df.format(returned*4000));
            }else{
                txtReturned.setText("");
            }
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtReceivedKeyTyped
    
