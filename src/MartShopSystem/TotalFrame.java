    private void cboDiscountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboDiscountActionPerformed
        try{
            DecimalFormat df=new DecimalFormat("$0.00");
            double dis=Double.valueOf(""+cboDiscount.getSelectedItem());
            double disPrice=subTotal*dis/100;
            txtDiscount.setText(""+disPrice);
            double total=subTotal - disPrice;
            txtGrandTotal.setText(""+total);
            txtTotalR.setText(""+total*4000);
            
       }catch(Exception e){
           JOptionPane.showMessageDialog(this, e);
       }
        // TODO add your handling code here:
    }//GEN-LAST:event_cboDiscountActionPerformed

